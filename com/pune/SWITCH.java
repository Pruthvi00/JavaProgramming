package com.pune;

public class SWITCH {
	public static void main(String args[]) {
		int choice = 2 ;
		switch(choice) {
		case 1 : 
			System.out.println("choice 1 ");
			break;
		case 2 :
			System.out.println("choice 2 ");
			break;
		default :
			System.out.println("No choice available");
		}
	}
}
