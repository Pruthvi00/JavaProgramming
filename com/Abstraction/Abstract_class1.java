package com.Abstraction;

public abstract class Abstract_class1 {								// abstract class
	
	
	public abstract void m1();										// abstract method  without implementation
	
	public abstract void m2();
	
	public void m3() {
		System.out.println("Non Abstract Method");					// Non abstract Method 
	}

}

